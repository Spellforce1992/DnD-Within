// Current language (stored in localStorage)
function getLang() {
    return localStorage.getItem('dw_lang') || 'nl';
}

function setLang(lang) {
    localStorage.setItem('dw_lang', lang);
}

// Translation function
function t(key) {
    var lang = getLang();
    var dict = TRANSLATIONS[lang] || TRANSLATIONS['nl'];
    return dict[key] || TRANSLATIONS['nl'][key] || key;
}

// Check if subclass should be visible based on level
function isSubclassVisible(config, state) {
    if (!config.subclass) return false;
    var classData = DATA[config.className];
    if (!classData || !classData.subclasses) return false;
    var subData = classData.subclasses[config.subclass];
    if (!subData) return false;
    return state.level >= (subData.level || 1);
}
